#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;
	char	*d;
	char	*s;

	d = (char *)dst;
	s = (char *)src;
	if (!(unsigned char *)dst && !(unsigned char *)src)
		return (NULL);
	i = 0;
	if (d > s)
	{
		while (len-- > 0)
			d[len] = s[len];
		return (d);
	}
	else
	{
		while (i < len)
		{
			d[i] = s[i];
			i++;
		}
	}
	return (dst);
}
