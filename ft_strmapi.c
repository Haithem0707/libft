#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	len;
	char	*new;

	if (!s)
		return (NULL);
	len = ft_strlen(s);
	new = (char *)malloc(len * sizeof(char) + 1);
	if (!new)
		return (new);
	i = 0;
	while (i < len)
	{
		new[i] = (*f)(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
